fun main(){
    /**
     * when(cond){
     * case1->{task}
     * case2->{task2}
     * else->{default task}
     * }
     */
    var name:String="fah im"
    when(name){
        "fahim"->{
            println("Name is fahim")
        }
        "yeafi"->{
            println("Name is yeafi")
        }
        else->{
            println("This is default block")
        }
    }
}