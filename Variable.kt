fun main(){
    println("Hello World")

    var name:String="fahim"     //var variable can be changed future
    println(name)
    var age:Int=20
    println(age)

    println("My name is $name and I'm $age years old")      // String templating

    var nullVar:String?=null        // this is a null variable which can store null value.
    println(nullVar)

    val newName:String="yefi"      // You can't change val variable in future, it's like constant

    var isTrue:Boolean=true     // Boolean variable


}